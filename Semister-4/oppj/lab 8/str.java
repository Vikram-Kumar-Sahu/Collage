public class str {
    public static void main(String[] args) {
        String s = "Hello";
        System.out.println("The upper case is : "+s.toUpperCase());
        System.out.println("The lower case is : "+s.toLowerCase());
        System.out.println("The length is : "+s.length());
        System.out.println("The index of l is :"+s.indexOf('l'));
}
}